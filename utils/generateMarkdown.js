// Function: generateMarkdown - This function will return a string that contains the generated markdown for the 
// README file.
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function generateMarkdown(data) 
{
  const badgeUrl = renderLicenseBadge(data.license);
  const licenseText = renderLicenseSection(data.license);
  
  let returnData = `# ${data.title}<br>${badgeUrl}<br>  
  ## Description<br>  
  ${data.description}<br>  
  ## Table of Contents<br>  
  * [Installation](#installation)<br>  
  * [Usage](#usage)<br>  
  * [License](#license)<br>  
  * [Contributing](#contributing)<br>  
  * [Tests](#tests)<br>  
  * [Questions](#questions)<br>  
  <a name="installation"></a>  
  ## Installation  
    
  ${data.install_instructions}  
  
  <a name="usage"></a>  
  
  ## Usage  
  
  ${data.usage_information}  

  <a name="license"></a>  
  
  ## License  
  
  ${licenseText}  
  
  <a name="contributing"></a>  
  
  ## Contributing  
  
  ${data.contribute_guidelines}  
  
  <a name="tests"></a>  
  
  ## Tests  
  
  ${data.test_instructions}  
  
  <a name="questions"></a>  
  
  ## Questions  
  
  GitHub Username: ${data.github_username}  
  Link to GitHub Profile: https://github.com/${data.github_username}  
  Email Address: ${data.email_address}  
  
  ${data.question_instructions}`  
  
  return (returnData);
}

// Export the generateMarkdown function of this module.
module.exports = generateMarkdown;
