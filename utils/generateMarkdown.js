// Function: renderLicenseBadge - This function returns a license badge based on which license is passed in
// If there is no license, return an empty string
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function renderLicenseBadge(license) 
{
  const apache = `[![License](https://img.shields.io/badge/License-Apache_2.0-blue.svg)](https://opensource.org/licenses/Apache-2.0)`;
  const boost = `[![License](https://img.shields.io/badge/License-Boost_1.0-lightblue.svg)](https://www.boost.org/LICENSE_1_0.txt)`;
  const mit = `[![License: MIT](https://img.shields.io/badge/License-MIT-yellow.svg)](https://opensource.org/licenses/MIT)`;
  const bsd = `[![License](https://img.shields.io/badge/License-BSD_3--Clause-blue.svg)](https://opensource.org/licenses/BSD-3-Clause)`;

  // Test the value passed in and return the string that contains the license badge info.
  if (license === "Apache 2.0")
  {
      return (apache);
  }
  else if (license === "Boost 1.0")
  {
      return (boost);
  }
  else if (license === "MIT")
  {
      return (mit);
  }
  else if (license === "BSD 3")
  {
      return (bsd);
  }
  // If there is no license, then return an empty string.
  else 
  {
      return ("");  
  }
}

